
public class Location {

    private int x;
    private int y;
    private int z;
    private int orientation;

    public void setx(int X){
        x = X;}
    public void sety(int Y){
        y = Y;}
    public void setz(int Z){
        z = Z;}
    public void setOrientation(int O){
        orientation = O;}

    public int getx(){
        return x;}
    public int gety(){
        return y;}
    public int getz(){
        return z;}
    public int getOrientation(){
        return orientation;}
}
